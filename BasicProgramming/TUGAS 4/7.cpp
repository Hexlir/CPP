
#include "bits/stdc++.h"

using namespace std;

int main(){
    int input;
    cout << "Masukkan angka : ";
    cin >> input;

    switch (input) {
        case 0:
            cout << "Nol" << endl;
            break;
        case 1:
            cout << "Satu" << endl;
            break;
        case 2:
            cout << "Dua" << endl;
            break;
        case 3:
            cout << "Tiga" << endl;
            break;
        case 4:
            cout << "Empat" << endl;
            break;
        case 5:
            cout << "Lima" << endl;
            break;
        case 6:
            cout << "Enam" << endl;
            break;
        case 7:
            cout << "Tujuh" << endl;
            break;
        case 8:
            cout << "Delapan" << endl;
            break;
        case 9:
            cout << "Sembilan" << endl;
            break;
        default:
            cout << "Angka tidak valid" << endl;
            break;

    }
}